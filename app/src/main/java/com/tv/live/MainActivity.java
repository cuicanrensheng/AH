        String realUrl = getRealPlayUrl(ch.getPlayUrl());
            // ====================== 在这里加！第二处日志 ======================
    SettingsActivity.log("解析后地址：" + realUrl);
    // ==================================================================
        playerStateListener.setCurrentChannelName(ch.getName());
