    SettingsActivity.log("频道名称：" + ch.getName());
    SettingsActivity.log("原始地址：" + ch.getPlayUrl());
    // ==================================================================

    // 解析真实地址
    String realUrl = getRealPlayUrl(ch.getPlayUrl());
